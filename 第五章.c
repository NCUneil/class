//求s=a+aa+aaa+aaaa+aa...a的值，其中a是一个数字。
//例如2+22+222+2222+22222(此时共有5个数相加)，
//几个数相加由键盘控制。
#include<math.h>
int main()
{
	int s=0;//相加结果
	int a=2;//其中一个数字
	int n=2;//n个数相加
	int m=0;//指数
	int num=0;//第n个数
	
	for(n--;n>=0;n--)
	{
		num=0;//重置
		for(m=n;m>=0;m--)
		{
			num=num+a*pow(10.0,m);
		}
		s=s+num;
	}
	printf("%d\n",s);

	return 0;
}