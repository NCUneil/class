//判断101-200之间有多少个素数，并输出所有素数。

int main()
{
	int i=0;//数字
	int j=0;//除数
	int k=0;//k=1素数；k=0合数；
	int h=0;//换行
	for(i=100;i<=200;i++)//101到200
	{
		k=1;
		for(j=2;j<(i/2);j++)//除数
		{
			if(i%j==0)//是否能被整除
				k=0;
		}
		if(k)
		{
			printf("%d ",i);
			h++;
			if(h%5==0)//每五个一换行
			printf("\n");
		}
	} 
	printf("\n");
	return 0;
}
