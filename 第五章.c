//有一对兔子，从出生后第3个月起每个月都生一对兔子，
//小兔子长到第三个月后每个月又生一对兔子，
//假如兔子都不死，问每个月的兔子总数为多少？
int main()
{
	int m=0;//大兔子
	int s=1;//小兔子
	int d=0;
	for(int i=1;i<3;i++)//前两个月无大兔子
	{	
		printf("第%d月有%d对小兔子无大兔子\n",i,s);
	}
	for(i=3;i<10;i++)//三月份开始有大兔子
	{
		d=s;//小兔子长大了
		printf("第%d月有%5d对大兔子有%5d对小兔子共有%5d只兔子\n",i,m,s,2*(m+s));
		s=s+m;//小兔子在长大大兔子生小兔子
		m=d;//小兔子长大了	
	}

	return 0;
}
